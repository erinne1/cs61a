def count_palindromes(L):
    """The number of palindromic words in the sequence of strings
    L (ignoring case).

    >>> count_palindromes(("Acme", "Madam", "Pivot", "Pip"))
    2
    """
    "*** YOUR CODE HERE ***"
    return len(list(filter(lambda s: s.lower() == s[::-1].lower(), L)))
    
